1. 协议分类与定位
    分类	         代表协议	               核心定位	                 速率 / 距离关键逻辑
  低速短距离	     UART/I2C/SPI	         板级 / 芯片间通信	         硬件简单，无复杂抗干扰设计，速率低、距离近
  中速中距离	       CAN/LIN	          车内 / 工业设备间通信	       差分传输 + 抗干扰设计，速率中等，支持几十米传输
  高速长距离   USB/Ethernet/SDIO	     设备 / 主机间大数据传输	     高速差分 + 信号增强，速率高，支持百米级传输
  无线协议	     蓝牙 / WiFi/LoRa	           无布线通信	           射频协议复杂，依赖模块实现，距离 / 速率差异大
2.各个协议的中文名称                
  UART —————— 通用异步收发传输器—————————————异步、两根线、无时钟、全双工、简单、抗干扰差，多用于调试。
  I²C ——————— 集成电路总线——————————————————同步、两根线、半双工、多设备、地址寻址、速度慢。
  SPI ——————— 串行外设接口——————————————————同步、多根线、全双工、板内最快、无地址、靠片选、抗干扰一般。
  CAN ——————— 控制器局域网——————————————————差分、抗干扰极强、多主、仲裁、可靠、汽车 / 工业专用
  LIN ——————— 本地互联网络——————————————————单线、低成本、低速、主从、CAN 的廉价替补。
  USB ——————— 通用串行总线——————————————————差分、高速、热插拔、可供电、主从结构。
  Ethernet —— 以太网————————————————————————高速、最远、支持网络、可光纤、用于联网 / 大数据。
  SDIO ———————安全数字输入输出接口———————————SD 卡接口、高速、多用于存储、WiFi 模块。
  BLE ——————— 低功耗蓝牙—————————————————————无线、低功耗、近距离、小数据。
  WiFi —————— 无线局域网—————————————————————无线、高速、可上网、功耗较高。
  LoRa —————— 远距离无线电（一般直接叫LoRa)———无线、超远距、低速率、低功耗、物联网远距离用。
3.核心通讯协议讲解：
  01.UART
         001. 核心特性：
                      🟢异步串行通讯：无时钟线，依靠波特率同步
                      🟢全双工：TX发送，RX接收，可同时进行
                      🟢点对点：通常一对一连接
                      🟢硬件流控：可选RTS/CTS控制数据流
                      🟢速率较低（通常<10Mbps）
         002. 数据帧格式
                 ┌───┬───────┬───────┬───────┬───────┬───────┬───┐
                 │ S │ D0 │ D1 │ D2 │ D3 │ D4 │ D5 │ D6 │ D7 │ P │ E │
                 └───┴───────┴───────┴───────┴───────┴───────┴───┘
                 S:起始位(1位)  D:数据位(5-9位)  P:校验位(可选)  E:停止位(1-2位)
  02.I2C（Inter-Integrated Circuit）
        001. 核心特性：
                     🟢用 2 根线 就能让多个芯片互相发数据：
                           SCL：时钟线    SDA：数据线
                           SCL、SDA 都是 开漏 + 上拉电阻
                           你只能主动拉低（变成 0V），不能主动拉高
                           想变高：松手（释放），由上拉电阻自己拉回高电平
                     🟢同步半双工：有时钟线，分时双向
                     🟢多主多从：支持多个主机和从机
                     🟢地址寻址：每个设备有7位/10位地址
                     🟢速率标准：标准模式100kbps，快速模式400kbps，高速模式3.4Mbps
        002. 总线结构：
                      ┌─────────┐     ┌─────────┐     ┌─────────┐
                            │ 主机1   │     │ 从机1   │     │ 从机2   │
                            └────┬────┘     └────┬────┘     └────┬────┘
                                 │               │               │
                          SDA ───┼───────────────┼───────────────┼───
                                 │               │               │
                          SCL ───┼───────────────┼───────────────┼───
                                 │               │               │
                              4.7kΩ           4.7kΩ           4.7kΩ
                                 │               │               │
                                VDD             VDD             VDD
        003. 通信时序：
                      空闲状态：SCL高， SDA高
                      开始条件：SCL高时，SDA从高到低
                      发送 1 位数据：
                                    SCL 拉低
                                    改 SDA（0 或 1）
                                    SCL 拉高（对方这时读取 SDA）
                                    SCL 拉低（准备下一位）
                      发送 1 个字节：
                                    就是上面重复 8 次
                      应答位：  每个字节后接收方拉低SDA
                               它紧跟在 8 位数据的后面，是第 9 个时钟。
                               它的作用，就是 “确认收到了”。
                      停止条件：SCL高时，SDA从低到高
                      数据有效：SCL高电平期间，SDA必须稳定
                      
                     🟢🟢🟢描述： 初始 SDA 和 SCL 都为高，先在 SCL 保持高时把 SDA 拉低作为起始信号
                                   然后SCL拉低，意味着SDA你可以改变成你要发送的数据了
                                   此过程中你SDA爱变不变，上一个发的1你还要发1就可以不变
                                   SCL拉低就是通知SDA你可以改数据啦，你要是变，就快变，不变就不变
                                   然后SCL拉高，表示我在采样了，记录一下这个时候SDA是高还是低
                                   然后SCL拉低，表示我要开始下一次采样了，SDA要变的话你就抓紧变
                                   重复8次，发送8个bit,一个字节
                                   以上过程都是主机控制
                                   然后应答位：
                                   主机拉低SCL，问从机接收到了吗
                                   如果最后一位发的0，那主机拉低SCL后，还要接着把SDA拉高，将SDA控制权给从机
                                   从机再拉低SDA表示接收到啦
                                   然后SCL拉高，采集SDA电平，看看从机收到没有
                                   然后SDA拉高，表示通信结束
                                   注：拉高 = 释放，拉高为方便理解的说法，本身不准确

                                   简要概述🟢🟢🟢：
                                            发数据时：
                                                     SCL：主机控制，低电平允许修改SDA，高电平采样SDA（不允许修改）
                                                     SDA：发数据主机控制，应答位从机控制比如
                                            开始/停止时,可以在SCL高时修改SDA，为了和数据区分：
                                                     SDA高，SDA拉低表示开始，SDA拉高表示结束
                                            应答位：  SCL拉低，并释放SDA（之前高就不用管），让从机拉低SDA表示收到
   03.SPI
           001.核心特征：
                        🟢全双工同步通信：SPI允许数据在主设备和从设备之间同时进行发送和接收，通信由主设备产生的同步时钟（SCLK）控制。
                        🟢主从架构：通信由一个主设备发起和控制，一个或多个从设备响应。主设备通过片选信号选择特定的从设备。
                        🟢简单硬件，无寻址：与I2C不同，SPI没有复杂的地址机制，通过独立的片选线进行设备选择，协议开销极小，效率高。
                        🟢高速率：SPI的时钟频率可以很高（通常可达数十MHz甚至更高），使其非常适合传输大量数据或对实时性要求高的场景
