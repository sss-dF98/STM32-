│   │   │   └── 配置笔记：启用数字校准功能，补偿晶振误差。
│   │   └── Calibration Value​ (校准值)
│   │       └── 配置笔记：±值调整，正值为减慢，负值为加快。基于512秒周期调整ppm误差。
