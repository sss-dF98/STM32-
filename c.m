3.字符数组的初始化（初始化是指在定义变量 / 数组的同时，给它赋一个初始值）
  字符数组本质是存储多个字符的数组，常用来处理字符串（C 语言字符串就是以 \0 结尾的字符数组）。
  最常用：字符串字面量初始化
          char str[] = "hello";
          自动在末尾补 \0（字符串结束符）；数组大小自动为 6（h e l l o \0）；等价于：char str[6] = {'h','e','l','l','o','\0'};
  逐个字符初始化：
          char str[6] = {'h','e','l','l','o'};
          手动指定字符，未赋值的元素默认补 \0
          若数组大小刚好等于字符数（如 char str[5] = {'h','e','l','l','o'};），则没有 \0，不能当作字符串使用
 指定大小初始化：
          char str[10] = "hello";
          数组大小 10，前 5 个字符为 h e l l o，后面自动补 \0 填充剩余空间；适合预留空间，方便后续拼接字符串
空初始化：
          char str[10] = "";
          等价于 char str[10] = {'\0'};，所有元素初始为 \0；常用于清空字符串或作为缓冲区
   注：
      带 \0 才是字符串：只有末尾有 \0，才能用 printf("%s", str) 等字符串函数
      数组大小要足够：至少要比字符串字面量多 1（给 \0 留位置）
      不可直接赋值：初始化后不能用 str = "world"; 赋值，必须用 strcpy(str, "world");
      str 本质是一个指向数组首地址的常量指针，它的值（内存地址）在定义后就不能被修改。
      str = "world" 本质是想让 str 指向新字符串 "world" 的地址，这和「修改常量」是一个道理，所以编译器直接禁止
      C 语言只支持单个元素赋值，比如 str[0] = 'w';，但不支持把一整个数组「整体替换」成另一个数组
      想修改字符数组的内容，必须用字符串拷贝函数：
      #include <string.h>
      char str[10] = "hello";
      strcpy(str, "world");  // ✅ 正确：把"world"拷贝到str的内存里
      strcpy 会逐个字符复制，直到遇到 \0，保证末尾有结束符
  4.数组名和&数组名所表示的地址区别
      对于数组 int a[3]：
      a → 代表首元素的地址，类型是 int*（指向 int 的指针）
      &a → 代表整个数组的地址，类型是 int （*）[3]（指向包含 3 个 int 的数组的指针）
      两者数值上相等（都指向数组起始内存地址），但指针类型和步长完全不同
      以 int a[3] = {0,0,0}; 为例：
      a（首元素地址）：
      a → 指向 a[0]
      a+1 → 指向 a[1]，步长 = sizeof(int)（4 字节）
      &a（整个数组地址）：
      &a → 指向数组起始位置（和 a 数值一样）
      &a+1 → 指向整个数组之后的位置，步长 = sizeof(a) = 3*sizeof(int)（12 字节）
      a 是「元素指针」， &a 是数组指针」
      a+1 是下一个元素，&a+1 是下一个数组
      只有 sizeof(a) 和 &a 时，数组名才代表整个数组，其他时候都代表首元素地址
  5.什么是地址和解引用？
      你可以把内存想象成一排带门牌号的小柜子：
      每个柜子有一个唯一的门牌号（地址）
      柜子里放着数据（值）
      指针：就是存「门牌号」的纸条
      解引用 *：就是「拿着纸条去对应柜子里，把里面的东西取出来」的动作
      例：一维数组
         int arr[3] = {10, 20, 30};
         arr 是数组名，等价于指向第一个元素 arr[0] 的指针（存着 arr[0] 的地址）
         arr + 1：地址往后跳 1 个 int，指向 arr[1]
         *(arr + 1)：解引用，就是去 arr[1] 的地址里，把里面的值 20 取出来
         🟢所以 arr[1] 和 *(arr + 1) 是完全一样的，都是「取第 2 个元素的值」
         🟢：为什么arr[i]  =  *(arr + i) ？
              
      例：二维数组
         以 int a[2][3] = {{1,2,3}, {4,5,6}} 为例：
         二维数组可以理解成「数组里装数组」：
         大数组 a 里有 2 个元素，每个元素是一个长度为 3 的小数组（一行）
         第 i 行的小数组叫 a[i]
         第一步：先拿到「行」
         🟢a：指向第 0 行 a[0] 的指针
         a + i：指向第 i 行 a[i] 的指针
         *(a + i)：解引用，把第 i 行这个小数组取出来，等价于 a[i]
         第二步：再拿到「行里的元素」
         a[i]：指向第 i 行第 0 个元素 a[i][0] 的指针
         a[i] + j：指向第 i 行第 j 个元素 a[i][j] 的指针
         *(a[i] + j)：解引用，把 a[i][j] 的值取出来
         所以：
         a[i][j] = *(a[i] + j) = *(*(a + i) + j)
         本质就是：先找行 → 再找行里的元素，两次解引用拿到最终值。
