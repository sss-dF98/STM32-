│   │       └── 配置笔记：将时间戳触发引脚连接到特定备用功能(AF1)，通常为PC13。
│   └── 🔒 Tamper​ (侵入检测)
│       ├── ✅ [ ] Enable (使能)
│       │   └── 配置笔记：使能防篡改检测，当侵入引脚电平变化时触发事件。
│       └── Tamper1 Routed to AF1​ (侵入检测路由到AF1)
│           └── 配置笔记：将侵入检测引脚连接到特定备用功能(AF1)，通常为PC13。
│
├── ⚙️ Parameter Settings​ (参数设置)
│   ├── 📅 General​ (通用设置)
│   │   ├── Hour Format​ (小时格式)
│   │   │   └── 配置笔记：24小时制​ 或 12小时制(AM/PM)。工业应用建议24小时制。
│   │   ├── Asynchronous Predivider value​ (异步预分频值)
│   │   │   └── 配置笔记：默认值127。将RTCCLK分频生成ck_apre时钟(通常1Hz)。计算：f_ck_apre = RTCCLK/(PREDIV_A+1)。
│   │   └── Synchronous Predivider value​ (同步预分频值)
│   │       └── 配置笔记：默认值255。进一步分频生成ck_spre时钟(1Hz)。计算：f_ck_spre = f_ck_apre/(PREDIV_S+1)。
