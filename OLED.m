OLED = Organic Light Emitting Diode（有机发光二极管）
OLED 屏（常见 SSD1306）本质就是： 一块自带显存的点阵屏，MCU 通过 I2C/SPI 把 “点阵数据” 发给它，它就自动显示。

01. SSD1306 OLED（128×64）的显存结构：
    128 列，64 行
    8 行为 1 页，分成 8 页，也就是说 1 页 = 8 行 × 128 列
    其中 1 页里面的 1 列为 1 字节 = 8 个竖点
    软件原理：
             你发送 1 个字节 = 控制竖着的 8 个像素点
          如：
             字节 0x80 → 二进制 10000000 → 最上面一个点亮
             字节 0xFF → 二进制 11111111 → 8 个点全亮
             字节 0x00 → 8 个点全灭
02. SSD1306 OLED（128×64）的通讯原理：
    001.并行通信（8080 并口）
        整个系统分成三大部分：
                            MCU（单片机） —— 控制者
                            SSD1306 驱动芯片 —— 显示控制器
                            OLED 面板 —— 发光点阵
         并行通信 = 8根线一次一起传 1位，共 8位（1 字节），速度更快，原理更简单。
         SSD1306 OLED 8080并口需要8个数据引脚，5个控制/电源引脚，共13个引脚
         不过在实际工程中，往往按照12个计算，RD（读）几乎不用，OLED只写不读
         a.电源与复位引脚（必须有）
                 VDD：芯片逻辑电源（3.3V）
                 VSS：地
                 VCC：OLED 驱动电源（模块一般已处理）
                RES#：复位脚（低电平复位）
二、并行通信 全部控制脚（一个都不能少）
                 CS#：片选      ：CS# = 1：芯片休眠，不听任何信号  CS# = 0：芯片被选中，准备通信
                 D/C#：数据/命令选择       ：D/C# = 0 → 接下来传输的是 命令    D/C# = 1 → 接下来传输的是 数据，D/C# 决定了字节进入芯片的哪个通道。
                 WR#：写信号           ：WR# 从 1 → 0（下降沿）：SSD1306 锁存 D0~D7 的数据  数据只有在这个时刻，才会真正进入芯片
                 RD#：读信号（驱动 OLED 一般不用，一般直接接高电平，但它是并行总线的一部分）
                 D0 ~ D7：8 位数据总线
                 这 1（RES）+ 4（控制）+ 8（数据）= 13 根线，就是完整并行 OLED 通信系统。
03. 完整写时序步骤（一步一步）
                 初始状态：
                 CS#=1，WR#=1，D/C#= 任意，D0~D7 = 高阻
             步骤 1：拉低 CS#
                 CS# = 0
                 → 芯片被选中，开始监听控制脚
             步骤 2：设置 D/C#
                 发命令：D/C# = 0
                 发数据：D/C# = 1
                 告诉芯片：
                 下一个字节是命令，还是显示数据。
             步骤 3：MCU 把 8 位数据放到 D0~D7
                 例如发送 0xAE：
                 D7 D6 D5 D4 D3 D2 D1 D0
                 1 0 1 0 1 1 1 0
             步骤 4：WR# 拉低（下降沿）
                 WR# 1 → 0
                 芯片在这一刻，读取 D0~D7 的数据
             步骤 5：WR# 拉高（写入完成）
                 WR# 0 → 1
                 字节正式写入芯片。
             步骤 6：CS# 拉高，结束
                 CS# = 1
                 通信结束。
04.OLED 点亮一个点的完整过程
             步骤 1：MCU 发命令 → 设置页地址
                     命令格式：0xB0 + 页号
             步骤 2：MCU 发命令 → 设置列地址
                     SSD1306 列地址是 7 位，需要分高 4 位和低 4 位两次发送：
                     低 4 位命令：0x00 + (列地址 & 0x0F)         注：0x0F对应二进制：0000 1111
                     高 4 位命令：0x10 + ((列地址 >> 4) & 0x0F)  注：& 这一步的意义就是高4位变零
                                 简单来说：0000 跟 低四位 。0001 跟 高四位（上面的加是2进制的逐位相加）
             步骤 3：MCU 发数据 → 写入一个字节到显存
             步骤 4：SSD1306 将显存转为 SEG/COM 驱动信号
             步骤 5：OLED 像素发光
            注：步骤1,2是命令，步骤3是数据，命令拉低 D/C#，数据拉高 D/C#
05.并行通讯的 SPI和 I2C与并行通讯的8080有何不同？
            三种接口本质都是MCU 向 SSD1306 发命令 / 数据，但在引脚数量、通信方式、速度、命令 / 数据区分方式上不同
            维度	            8080 并行	            SPI	                        I2C
            引脚数量	            最多	                中等	                    最少
            传输方式	            并行 8位同时传	        串行 1 位逐位传	            串行 1 位逐位传
            速度	            最快（MHz级，8位并行）	中（几十 MHz，串行）	        最慢（几十 kHz~1 MHz）
            命令/数据区分	    硬件引脚 D/C#	        硬件引脚 D/C#	            协议内控制字节（0x00/0x40）
            读写方向	            支持读/写	            只写（无MISO）	            只写（读极少用）
            总线共享	            难（引脚多）	            易（同SPI 总线挂多设备）	    极易（同 I2C 总线挂多设备）
            适用场景	            高速刷新、大屏	        平衡速度与引脚	            引脚紧张、低速场景
      8080 并行：
                核心引脚：D0~D7（8 根数据线）+ CS#/D/C#/WR#/RD#（4 根控制线）
                特点：
                    一次传 8 位，速度最快，但占用引脚极多，PCB 布线复杂
                    D/C# 引脚直接决定命令 / 数据，硬件区分，无歧义
      SPI：
                核心引脚：SCLK（时钟）/DIN（数据入）/CS#（片选）/D/C#（命令 / 数据）
                特点：
                     串行传输，引脚少，布线简单
                     保留 D/C# 引脚，和并行一样硬件区分命令 / 数据，逻辑直观
                     速度比并行慢，但比 I2C 快很多，是嵌入式最常用方案
      I2C
                核心引脚：SCL（时钟）/SDA（数据）
                特点：
                     引脚最少，仅 2 根线即可通信，极度节省引脚
                     无 D/C# 引脚，靠协议里的控制字节区分命令 / 数据（0x00 = 命令，0x40 = 数据）
                     速度最慢，且多设备共享时会有总线冲突
                     适合小型、低功耗、引脚紧张的设备（如穿戴、IoT）
    
