1. 什么是RTC
RTC（Real-Time Clock）实时时钟：独立的定时计数器，在主系统断电时依靠备用电池继续运行
嵌入式系统的“生物钟”：提供精确的日历时间和计时功能
与通用定时器的区别：RTC以秒为基本单位，专注于长期、连续的时间记录
2. 关键特性：
   01. 独立供电：VBAT引脚连接备用电池(3V)
       Vdd（主电源）：当设备主电源存在时，由它供电。
       Vbat（后备电池）：通常连接一颗纽扣电池（如CR2032）。当主电源断开时，自动无缝切换至电池供电，保证RTC持续运行。这是RTC“永不停摆”的物理基础。
   02. 独立时钟源：
      ├── LSE (32.768KHz) ★最常用  (因为32768 = 2¹⁵，经过15次二分频后正好得到1Hz（1秒1次）的信号，计算简单又精确)
      ├── LSI (~32KHz)   精度较低
      └── HSE分频        高精度需求
   03. 独立复位：系统复位不影响RTC运行
  时钟树工作流程：
3.时钟线
  32.768KHz晶振 → 异步预分频器 → 同步预分频器 → 1Hz信号
        ↓               ↓               ↓
     原始频率       降到约1KHz       精确的1秒脉冲  
4. 核心寄存器
   RTC_TR：    时间寄存器，以BCD格式存储时、分、秒。
   RTC_DR：    日期寄存器，以BCD格式存储年、月、日、星期。
   RTC_SSR：   亚秒寄存器，提供更精细的时间分辨率。
   RTC_PRER：  预分频器寄存器，用于配置同步和异步分频系数，将RTC时钟源分频至1Hz。
   RTC_CR：    控制寄存器，用于启用闹钟、时间戳、输出等功能。
   RTC_ALRMxR：闹钟寄存器，存储闹钟匹配条件
01.时间寄存器（RTC_TR）
   存储时、分、秒，但存储格式很特殊——BCD码。通常我们读写的是其影子寄存器。
   什么是BCD码？
     十进制：14 → 二进制：1110
   🟢BCD码：每4位二进制对应1位十进制数：从 0000(0) 到 1001(9)，1010(10) 到 1111(15) 在BCD码中无意义，属于非法编码
            14 → 二进制：🟢 0001 0100（1和4分开存储）🟢
   为什么要用BCD码？因为直接读取就是十进制数，不用转换。
02. 日期寄存器（RTC_DR）
    存储年、月、日、星期。STM32的RTC能自动处理：
    每月天数不同（28/29/30/31天）
    闰年判断
    星期自动计算
03. RTC_SSR (亚秒寄存器)
    作用： 提供比“秒”更精细的计时分辨率。
    时钟来源：异步预分频器
    原理： 它是一个递减计数器，从RTC_PRER中配置的同步预分频值（synch_prescaler）开始递减 （注：这说的是重装载值来源，不是时钟来源）
    每秒减到0时触发秒更新。因此，SSR的值反映了当前秒已经过去了多少个子秒周期。
    用途： 可用于高精度时间测量或时间戳。
04. RTC_PRER (预分频器寄存器)
    作用： 将输入的RTC时钟（RTCCLK， 如32.768kHz）分频，产生1Hz的时钟驱动日历计数器。
    关键位域：
    [6:0] (位22:16)： 异步预分频器。用于对高速时钟（如1MHz）进行粗分频，其输出驱动唤醒定时器。此分频器在低功耗模式下仍运行。
    [14:0] (位14:0)： 同步预分频器。用于产生精确的1Hz时钟。总频率计算公式：Calendar Clock = RTCCLK / [(PREDIV_A + 1) * (PREDIV_S + 1)]。目标值为1。
    🟢为什么要有这俩分频器？
    🔴异步预分频器：活在“备份世界”
    时钟域：它直接由RTC专用时钟（LSE/LSI）驱动，与APB总线时钟毫无关系。两者是“异步”的，节拍不同。
    电源域：它属于备份电源域（VBAT）。
    带来的特性：
    永远独立运行：无论主系统是跑是睡，甚至主电源断电，只要VBAT有电（纽扣电池），它就永不停止。
    难以直接干预：CPU不能直接、安全地操作它，因为两边时钟不同步，强行读写会导致错误。
    用途：它为“必须永远在线”的功能服务——驱动唤醒定时器。
    🔴同步预分频器：活在“主世界”
    时钟域：它由APB总线时钟驱动并同步。这意味着它的每一个操作节拍，都和CPU、内存等核心模块的节拍对齐。
    电源域：它属于芯片的主电源域（VDD）。
    带来的特性：
    可以被精确管理：CPU可以随时、无风险地读写和校准它，因为大家时钟一致。
    可以被彻底关闭：当芯片进入深度睡眠以省电时，整个主电源域都可以断电。因此，同步预分频器会完全停止。
    用途：它只为“需要高精度和易用性”的功能服务——生成1Hz信号驱动日历。
    它由APB总线时钟驱动并同步。这意味着它的每一个操作节拍，都和CPU、内存等核心模块的节拍对齐。
    🔴实际怎么工作？
      比如手机：
              开屏：正常状态，我就读秒
              关屏：要节约能源，不能读了，同步预分频器关掉，存储关闭时间
                   异步预分频器关闭，开屏再让同步喊自己，假装自己一直在工作。（注：异步比同步时间精度要高的，一个秒，一个毫秒）
     典型配置（LSE=32768）： PREDIV_A = 127, PREDIV_S = 255。计算：32768 / (128 * 256) = 1 Hz。
 05. 控制与状态寄存器
     通用名：RTC_CR(Control Register), RTC_ISR(Interrupt and Status Register)
     功能：这是RTC的“开关和仪表盘”。
     RTC_CR：包含使能位（RTC_EN）、格式选择位（FMT，12/24小时制）、闹钟中断使能位（ALRAIE）、时间戳中断使能位（TSIE）等。你通过写这些位来配置RTC的行为。
     RTC_ISR：包含状态标志位。例如，当闹钟发生时，硬件会自动将ALRAF位设为1；当寄存器正在被更新时，INIT位可被操作以进入初始化模式。你通过读这些位来了解RTC的状态。
 06. 闹钟寄存器
     通用名：RTC_ALRMAR, RTC_ALRMBR(Alarm A/B Register)
     功能：存储闹钟设定的时间。其格式与RTC_TR/RTC_DR类似，但每个字段都配有一个掩码位（MSK）。
     例如，ALRMAR寄存器包含MSK1（秒掩码），SU/ST（秒值），MSK2（分掩码），MNU/MNT（分值）...以此类推。
     掩码位是关键：若MSKx=1，则比较时忽略该字段。例如，设置小时=14，分钟=30，并将秒掩码MSK1设为1，则每天14:30:00到14:30:59之间的每一秒都会触发闹钟🟢。
 07. 写保护与备份寄存器
     通用名：RTC_WPR(Write Protect Register), RTC_BKPxR(Backup Register)
     功能：安全与数据保持。
     RTC_WPR：为了防止软件跑飞意外修改RTC配置，向此寄存器写入错误密钥会锁定对RTC寄存器的写操作。正确的解锁序列是：先写0xCA，再写0x53。
     RTC_BKPxR：这是一组由VBAT供电的通用寄存器。当主电源断开后，这里的数据不会丢失。常用于存储设备序列号、首次上电标志、休眠前的系统状态等。
