│   │   ├── ✅ [ ] Enable (使能)
│   │   │   └── 配置笔记：使能周期性唤醒中断，用于低功耗模式定时唤醒。
│   │   └── WakeUp Clock​ (唤醒时钟源)
│   │       └── 配置笔记：选择唤醒计数器时钟源，如RTCCLK/2/4/8...等，决定唤醒周期。
