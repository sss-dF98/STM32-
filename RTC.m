│   │   │   └── 配置笔记：设置初始月份(January-December)。
│   │   ├── Date​ (日期)
│   │   │   └── 配置笔记：设置初始日期(1-31)，需与月份匹配。
│   │   └── Year​ (年份)
│   │       └── 配置笔记：设置初始年份(0-99或0-4095，取决于芯片)。通常存储年份后两位。
